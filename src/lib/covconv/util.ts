export interface ICovConvDic {
  ja_JP: string,
  kv_JP: string,
  regex: RegExp,
}

export class Util {
  public static get CovConvDic() {
    const result: ICovConvDic[] = [] as any;

    dic.forEach((value) => {
      const dic: ICovConvDic = {} as any;
      dic.ja_JP = value.ja_JP;
      dic.kv_JP = value.kv_JP;
      dic.regex = new RegExp(value.ja_JP, "gmi");
      result.push(dic);
    })
    return result;
  }

  public static convertToHalfWidthKana(text: string) {
    for (var i = 0; i < fullKana.length; i++) {
      var re = new RegExp(fullKana[i], "gmi");
      text = text.replace(re, halfKana[i]);
    }
    return text;
  }

  public static isTextElement(element: HTMLDocument | HTMLElement | Node) {
    if ("parentNode" in element && element.parentNode != null) {
      var nodeName = element.parentNode.nodeName.toLowerCase();
      if ((nodeName == "style") || (nodeName == "script") || (nodeName == "frame")) {
        return false;
      }
    }
    if (element.nodeName.toLowerCase() != "#text") {
      return false;
    }

    return true;
  }
}
